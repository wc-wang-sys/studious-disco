import { Card, CardContent } from "@/components/ui/card";
import { motion } from "framer-motion";

export default function App() {
  const [page, setPage] = useState("login");
  const [role, setRole] = useState(null);
  const [userName, setUserName] = useState("");

  return (
    <div className="w-full min-h-screen bg-gray-100 p-6">
      {page === "login" && (
        <LoginPage
          onLogin={(role, name) => {
            setRole(role);
            setUserName(name);
            setPage("home");
          }}
        />
      )}

      {page === "home" && (
        <HomePage
          userName={userName}
          role={role}
          goTo={(p) => setPage(p)}
        />
      )}

      {page === "create" && (
        <StoryCreationFlow goTo={(p) => setPage(p)} />
      )}

      {page === "library" && (
        <LibraryPage goTo={(p) => setPage(p)} />
      )}
    </div>
  );
}

// ----------------- LOGIN PAGE -----------------
function LoginPage({ onLogin }) {
  const [name, setName] = useState("");
  const [role, setRole] = useState("student");

  return (
    <motion.div className="max-w-md mx-auto mt-20 p-6 bg-white rounded-2xl shadow-lg">
      <h1 className="text-2xl font-bold mb-4">Login Page</h1>

      <label className="block mb-2 text-sm font-semibold">User Name</label>
      <input
        className="w-full border rounded p-2 mb-4"
        value={name}
        onChange={(e) => setName(e.target.value)}
      />

      <label className="block mb-2 text-sm font-semibold">Role</label>
      <select
        className="w-full border rounded p-2 mb-6"
        value={role}
        onChange={(e) => setRole(e.target.value)}
      >
        <option value="student">Student</option>
        <option value="teacher">Teacher</option>
        <option value="tester">Tester</option>
      </select>

      <Button
        onClick={() => onLogin(role, name || "Guest")}
        className="w-full"
      >
        Log in
      </Button>
    </motion.div>
  );
}

// ----------------- HOME PAGE -----------------
function HomePage({ userName, role, goTo }) {
  return (
    <motion.div className="max-w-2xl mx-auto mt-10" initial={{ opacity: 0 }} animate={{ opacity: 1 }}>
      <Card>
        <CardContent className="p-6">
          <h2 className="text-xl font-bold">Welcome, {userName}</h2>
          <p className="text-gray-600 mb-4 capitalize">Role: {role}</p>

          <div className="flex flex-col gap-4 mt-4">
            <Button onClick={() => goTo("create")}>Create a New Story</Button>
            <Button variant="outline" onClick={() => goTo("library")}>See Previous e‑Books</Button>
          </div>
        </CardContent>
      </Card>
    </motion.div>
  );
}

// ----------------- STORY CREATION FLOW -----------------
function StoryCreationFlow({ goTo }) {
  const [step, setStep] = useState(0);
  const [character, setCharacter] = useState("");
  const [place, setPlace] = useState("");
  const [time, setTime] = useState("");
  const [plot, setPlot] = useState("");

  const steps = [
    "Greeting",
    "Character Creation",
    "Place Selection",
    "Time Confirmation",
    "Plot",
    "Generate",
  ];

  return (
    <motion.div className="max-w-xl mx-auto mt-10" initial={{ opacity: 0 }} animate={{ opacity: 1 }}>
      <Card>
        <CardContent className="p-6">
          <h2 className="text-xl font-bold mb-4">Story Creation</h2>
          <p className="font-semibold mb-4">Step {step + 1}: {steps[step]}</p>

          {step === 0 && (
            <div>
              <p className="mb-4">你好，今天想创作什么故事呀？</p>
              <Button onClick={() => setStep(1)}>Start</Button>
            </div>
          )}

          {step === 1 && (
            <StepInput
              label="Describe the character"
              value={character}
              onChange={setCharacter}
              onNext={() => setStep(2)}
            />
          )}

          {step === 2 && (
            <StepInput
              label="Where does the story take place?"
              value={place}
              onChange={setPlace}
              onNext={() => setStep(3)}
            />
          )}

          {step === 3 && (
            <StepInput
              label="When does the story take place?"
              value={time}
              onChange={setTime}
              onNext={() => setStep(4)}
            />
          )}

          {step === 4 && (
            <StepInput
              label="What happens in the story?"
              value={plot}
              onChange={setPlot}
              onNext={() => setStep(5)}
            />
          )}

          {step === 5 && (
            <div>
              <p className="mb-4 font-semibold">Your story draft is ready!</p>
              <Card className="mb-4">
                <CardContent className="p-4 text-sm">
                  <p><strong>Character:</strong> {character}</p>
                  <p><strong>Place:</strong> {place}</p>
                  <p><strong>Time:</strong> {time}</p>
                  <p><strong>Plot:</strong> {plot}</p>
                </CardContent>
              </Card>

              <Button onClick={() => goTo("library")} className="mr-2">Save Story</Button>
              <Button variant="outline" onClick={() => goTo("home")}>Exit</Button>
            </div>
          )}
        </CardContent>
      </Card>
    </motion.div>
  );
}

function StepInput({ label, value, onChange, onNext }) {
  return (
    <div>
      <label className="block mb-2 font-semibold">{label}</label>
      <textarea
        className="w-full border rounded p-2 mb-4"
        rows={3}
        value={value}
        onChange={(e) => onChange(e.target.value)}
      />
      <Button onClick={onNext}>Next</Button>
    </div>
  );
}

// ----------------- LIBRARY PAGE -----------------
function LibraryPage({ goTo }) {
  const sampleBooks = [
    { id: 1, title: "The Brave Cat" },
    { id: 2, title: "The Magic Forest" }
  ];

  return (
    <motion.div className="max-w-xl mx-auto mt-10" initial={{ opacity: 0 }} animate={{ opacity: 1 }}>
      <h2 className="text-xl font-bold mb-6">Generated e‑Books</h2>

      <div className="space-y-4">
        {sampleBooks.map((b) => (
          <Card key={b.id}>
            <CardContent className="p-4 flex justify-between items-center">
              <span>{b.title}</span>
              <Button variant="outline">Read</Button>
            </CardContent>
          </Card>
        ))}
      </div>

      <Button className="mt-6" onClick={() => goTo("home")}>Back</Button>
    </motion.div>
  );
}
